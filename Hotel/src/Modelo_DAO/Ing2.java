package Modelo_DAO;

public class Ing2 {
	private String claveOld;
	private String claveNew;
	
	public Ing2(String claveOld, String claveNew) {
		super();
		this.claveOld = claveOld;
		this.claveNew = claveNew;
	}

	public String getClaveOld() {
		return claveOld;
	}

	public void setClaveOld(String claveOld) {
		this.claveOld = claveOld;
	}

	public String getClaveNew() {
		return claveNew;
	}

	public void setClaveNew(String claveNew) {
		this.claveNew = claveNew;
	}
	
}
